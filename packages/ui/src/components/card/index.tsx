import * as React from "react";
import { twMerge } from "tailwind-merge";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={twMerge(
      "rounded-lg border border-border bg-white dark:bg-neutral-900 dark:border-neutral-800 shadow-sm",
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

